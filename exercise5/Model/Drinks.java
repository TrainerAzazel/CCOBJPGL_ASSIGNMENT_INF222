package Model;

class Drinks {

    private String flavor;
    private String price;

    public String getFlavor(){
        return this.flavor;
    }
    public void setFlavor(String flavor){
        this.flavor = flavor;
    }

    public String getPrice(){
        return this.price;
    }
    public void setPrice(String price){
        this.price = price;
    }


}
